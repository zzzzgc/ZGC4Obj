package com.xinxing.o.boss.api.customer.domain;

public class FlowRespCard {
	
	private String code; //失败代码
	private String msg;// 失败原因
	private String cardId;//卡号
	private String cardPwd;// 卡密
	
	
	
	
	public FlowRespCard( String code, String msg, String cardId,
			String cardPwd) {
		this.code = code;
		this.msg = msg;
		this.cardId = cardId;
		this.cardPwd = cardPwd;
	}
	
	public FlowRespCard() {
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getCardId() {
		return cardId;
	}
	public void setCardId(String cardId) {
		this.cardId = cardId;
	}
	public String getCardPwd() {
		return cardPwd;
	}
	public void setCardPwd(String cardPwd) {
		this.cardPwd = cardPwd;
	}
	
	
}
