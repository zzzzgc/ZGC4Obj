package com.xinxing.subpackage.core.po;

public class PackInfo {
	
	private String packId;
	private String packProductId;
	public String getPackId() {
		return packId;
	}
	public void setPackId(String packId) {
		this.packId = packId;
	}
	public String getPackProductId() {
		return packProductId;
	}
	public void setPackProductId(String packProductId) {
		this.packProductId = packProductId;
	}
	@Override
	public String toString() {
		return "PackInfo [packId=" + packId + ", packProductId=" + packProductId + "]";
	}
	

}
